class Voxel {
private:
    ivec3 position;
    imat4 model = glm::mat4(1.0f);
    int type = VOXEL_AVT;
public:
    Voxel(ivec3 pos, int typ) {
        position = pos;
        type = typ;
        model = glm::translate(imat4(1), position);
    }
    void setPosition(ivec3 new_pos) {
        position = new_pos;
        model = glm::translate(imat4(1), position);
    }
    int getType() {
        return type;
    }
    glm::mat4 getModel() {
        return model;
    }
};