class Voxel {
private:
    ivec3 position;
    imat4 model = glm::mat4(1.0f);
    uint texture = 0;
public:
    Voxel(ivec3 pos, uint tex) {
        position = pos;
        texture = tex;
        model = glm::translate(imat4(1), position);
    }
    void setPosition(ivec3 new_pos) {
        position = new_pos;
        model = glm::translate(imat4(1), position);
    }
    int getTexture() {
        return texture;
    }
    glm::mat4 getModel() {
        return model;
    }
};